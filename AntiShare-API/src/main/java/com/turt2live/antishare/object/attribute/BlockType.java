package com.turt2live.antishare.object.attribute;

/**
 * Represents the various block types
 *
 * @author turt2live
 */
public enum BlockType {
    /**
     * Creative mode block
     */
    CREATIVE,
    /**
     * Survival mode block
     */
    SURVIVAL,
    /**
     * Adventure mode block
     */
    ADVENTURE,
    /**
     * Spectator mode block
     */
    SPECTATOR,
    /**
     * No associated type
     */
    UNKNOWN;
}
